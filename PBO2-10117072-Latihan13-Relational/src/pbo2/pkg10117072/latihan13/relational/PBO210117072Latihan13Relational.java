public class PBO210117072Latihan13Relational {

    public static void main(String[] args) {
       int bill = 10;
       int bil2 = 20;
        System.out.println("a == b = " + (bill == bil2));
        System.out.println("a != b = " + (bill != bil2));
        System.out.println("a > b  = " + (bill > bil2));
        System.out.println("a < b  = " + (bill < bil2));
        System.out.println("b >= a = " + (bil2 >= bill));
        System.out.println("b <= a = " + (bil2 <= bill));
    }
    
}
